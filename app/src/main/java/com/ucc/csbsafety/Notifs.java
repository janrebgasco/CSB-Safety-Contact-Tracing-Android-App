package com.ucc.csbsafety;

public class Notifs {
    public String Header;
    public String Description;
    public String OnclickInfo;
    public String Time;
    public String Date;

    public Notifs(String Header, String Description, String OnclickInfo, String Time, String Date) {
        this.Header = Header;
        this.Description = Description;
        this.OnclickInfo = OnclickInfo;
        this.Time = Time;
        this.Date = Date;
    }
}
